def load_env():
    env = {}
    with open('.env') as f:
        for line in f:
            if '=' in line and not line.startswith('#'):
                key, val = line.strip().split('=', 1)
                env[key] = val
    return env

def populate():
    print("=" * 80)
    print("📚 Populating Sample Saudi Laws")
    print("=" * 80)

    env = load_env()
    supabase_url = env['SUPABASE_URL']
    supabase_key = env['SUPABASE_SERVICE_ROLE_KEY']
    openai_key = env['OPENAI_API_KEY']

    openai_client = OpenAI(api_key=openai_key)

    headers = {
        "apikey": supabase_key,
        "Authorization": f"Bearer {supabase_key}",
        "Content-Type": "application/json",
        "Prefer": "return=representation"
    }

    saved_count = 0

    for i, law in enumerate(SAMPLE_LAWS, 1):
        print(f"\n[{i}/{len(SAMPLE_LAWS)}] {law['name_ar']}")

        try:
            # Insert law
            law_response = requests.post(
                f"{supabase_url}/rest/v1/laws",
                headers=headers,
                json={
                    'name_ar': law['name_ar'],
                    'law_number': law['law_number'],
                    'url': law['url'],
                    'full_text_ar': law['full_text_ar']
                },
                timeout=30
            )

            if law_response.status_code not in [200, 201]:
                print(f"  ⚠️ May already exist: {law_response.status_code}")
                continue

            law_data = law_response.json()
            law_id = law_data[0]['id'] if isinstance(law_data, list) else law_data['id']
            print(f"  ✅ Law saved (ID: {law_id})")

            # Generate embedding
            print(f"  🧠 Generating embedding...")
            text_for_embedding = f"{law['name_ar']}\n\n{law['full_text_ar']}"

            try:
                emb_response = openai_client.embeddings.create(
                    model="text-embedding-3-small",
                    input=text_for_embedding[:8000]
                )
                embedding = emb_response.data[0].embedding

                # Save embedding
                emb_save = requests.post(
                    f"{supabase_url}/rest/v1/law_embeddings",
                    headers=headers,
                    json={
                        'law_id': law_id,
                        'text_chunk': law['name_ar'],
                        'embedding': embedding,
                        'chunk_index': 0
                    },
                    timeout=30
                )

                if emb_save.status_code in [200, 201]:
                    print(f"  ✅ Embedding saved")
                    saved_count += 1

            except Exception as e:
                print(f"  ⚠️ Embedding error: {e}")

            # Save articles
            for article in law['articles']:
                try:
                    requests.post(
                        f"{supabase_url}/rest/v1/law_articles",
                        headers=headers,
                        json={
                            'law_id': law_id,
                            'article_number': article['number'],
                            'article_text_ar': article['text']
                        },
                        timeout=30
                    )
                except:
                    pass

            print(f"  ✅ {len(law['articles'])} articles saved")

            time.sleep(1)

        except Exception as e:
            print(f"  ❌ Error: {e}")

    print("\n" + "=" * 80)
    print(f"✅ Complete! Saved {saved_count} laws with embeddings")
    print("=" * 80)
    print("\n🎯 Now try asking questions in the app!")
    print("Examples:")
    print("  - ما هي عقوبة السرقة في النظام السعودي؟")
    print("  - كم ساعة عمل يومياً في نظام العمل؟")
    print("  - ما هي حقوق المستهلك؟")

if __name__ == '__main__':
    populate()
